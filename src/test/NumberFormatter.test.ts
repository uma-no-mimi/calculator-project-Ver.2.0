import { describe, it, expect } from 'vitest';
import { NumberFormatter } from '../NumberFormatter';
import { Config } from '../Config';

/**
 * NumberFormatter クラスのテスト
 * - NumberFormatterの基本的な各フォーマット機能を確認
 */
describe('NumberFormatter テスト', () => {
    /**
     * テスト対象のインスタンス
     * - Config.MAX_DIGITS を使用してインスタンスを作成
     * - 各テストケースでこのインスタンスを使用してフォーマット結果を検証
     */
    const formatter = new NumberFormatter(Config.MAX_DIGITS);

    /**
     * フォーマット関数のショートカット
     * - 各テストケースで formatter.formatForDisplay を簡単に呼び出せるようにするための関数
     * - format(123) は formatter.formatForDisplay(123) と同等
     */
    const format = (value: number) =>
        formatter.formatForDisplay(value);

    /** 基本的なフォーマット*/

    /**
     * 整数がそのまま表示されることを確認
     * - 例: 123 → "123"
     */
    describe('基本フォーマット', () => {
        /**
         * 整数がそのまま表示されることを確認
         * - 例: 123 → "123"
         */
        it('整数がそのまま表示される(123 → "123")', () => {
            expect(format(123)).toBe('123');
        });

        /**
         * 小数がそのまま表示されることを確認
         * - 例: 12.34 → "12.34"
         */

        it('小数がそのまま表示される(12.34 → "12.34")', () => {
            expect(format(12.34)).toBe('12.34');
        });
    });


    /**
     * ゼロ系の数値が正しく表示されることを確認
     * - 0が関係するケースをテスト
     */
    describe('ゼロ系', () => {
        /**
         * 0は"0"として表示されることを確認
         * -0 → "0"
         */
        it('0は"0"として表示される(0 → "0")', () => {
            expect(format(0)).toBe('0');
        });

        /**
         * -0は"0"として表示されることを確認
         * - -0 → "0"
         */
        it('-0は"0"として表示される(-0 → "0")', () => {
            expect(format(-0)).toBe('0');
        });
    });


    /**
     * 負の数が正しく表示されることを確認
     * - 負の整数や小数が正しい形式で表示されることをテスト
     */
    describe('負の数', () => {
        /**
         * 負の整数が正しく表示されることを確認
         * - 例: -123 → "-123"
         */
        it('負の整数が正しく表示される(-123 → "-123")', () => {
            expect(format(-123)).toBe('-123');
        });

        /**
         * 負の小数が正しく表示されることを確認
         * - 例: -12.34 → "-12.34"
         */
        it('負の小数が正しく表示される(-12.34 → "-12.34")', () => {
            expect(format(-12.34)).toBe('-12.34');
        });

        /**
         * 負の数で、小数点以下の末尾の0が削除されることを確認
         * - 例: -1.2300 → "-1.23", -5.000 → "-5"
         */
        it('負の数で、小数点以下の末尾の0は削除される(-1.2300 → "-1.23", -5.000 → "-5")', () => {
            expect(format(-1.2300)).toBe('-1.23');
            expect(format(-5.000)).toBe('-5');
        });

        /**
         * 負の数で、マイナス記号は桁数カウントに含まれないことを確認
         * - 例: -999999999 (9桁) → "-999999999" (マイナスはカウントされないため通常表示)
         */
        it('マイナス記号は桁数カウントに含まれない(-999999999 → "-999999999")', () => {
            // マイナスを除いて桁数がMAX_DIGITS以内であれば通常表示
            const maxDigitValue = Number('-' + '9'.repeat(Config.MAX_DIGITS));
            expect(format(maxDigitValue)).toBe(maxDigitValue.toString());
        });
    });


    /**
     * 小数の処理が正しく行われることを確認
     */
    describe('小数処理', () => {
        /**
         * MaxDigitsを超えた小数は指数表記に変換され、指定された桁数に収まるように表示されることを確認
         * - 例: 1.23456789 → "1.23456" (小数点以下7桁 + 整数部1桁 = 合計8桁)
         */
        it('MaxDigitsを超えた小数は指数表記になる(1.23456789 → "1.2345679e+0")', () => {
            expect(format(1.23456789)).toBe('1.2345679e+0'); // 小数点以下7桁 + 整数部1桁 = 合計8桁で指数表記
        });

        /**
         * 小数点以下の末尾の0は削除されることを確認
         * - 例: 1.2300 → "1.23", 5.0000 → "5", 100.0 → "100", 0.0000 → "0"
         */
        it('小数点以下の末尾の0は削除される(1.2300 → "1.23", 5.0000 → "5", 100.0 → "100", 0.0000 → "0")', () => {
            expect(format(1.2300)).toBe('1.23');
            expect(format(5.0000)).toBe('5');
            expect(format(100.0)).toBe('100');
            expect(format(0.0000)).toBe('0');
        });

        /**
         * 小数点以下に有効数字がある場合は保持されることを確認
         * - 例: 3.14001 → "3.14001", 0.0001 → "0.0001"
         */
        it('小数点以下に有効数字がある場合は保持される(3.14001 → "3.14001", 0.0001 → "0.0001")', () => {
            expect(format(3.14001)).toBe('3.14001');
            expect(format(0.0001)).toBe('0.0001');
        });
    });


    /**
     * 桁数カウントの仕様が正しく実装されていることを確認
     */
    describe('桁数カウント仕様', () => {
        /**
         * 小数点は桁数カウントに含まれないことを確認
         * - 例: 123.45 → 数字は5桁 (1,2,3,4,5) .は除外されるため通常表示
         */
        it('小数点は桁数カウントに含まれない(123.45 → "123.45")', () => {
            const value = 123.45; // 数字5桁、小数点はカウントされない
            expect(format(value)).toBe('123.45');
        });

        /**
         * マイナス記号は桁数カウントに含まれないことを確認
         * - 例: -123 → 数字は3桁 (1,2,3) マイナスは除外されるため通常表示
         */
        it('小数点とマイナス両方があっても正しくカウントされる(-123.45 → "-123.45")', () => {
            const value = -123.45; // 数字5桁、マイナスと小数点はカウントされない
            expect(format(value)).toBe('-123.45');
        });

        /**
         * 境界値: 最大桁数ちょうどは通常表示されることを確認
         * - 例: -999999999 (9桁) → "-999999999" (通常表示)
         */
        it('境界値: 最大桁数ちょうどは通常表示(-999999999 → "-999999999")', () => {
            const exactlyMax = Number('9'.repeat(Config.MAX_DIGITS));
            expect(format(exactlyMax)).toBe(exactlyMax.toString());
        });
        /**
         * 境界値: 最大桁数+1は指数表記になることを確認
         * - 例: 1000000000 (10桁) → "1e+9" (指数表記)
         */
        it('境界値: 最大桁数+1は指数表記(1000000000 → "1e+9")', () => {
            const overMax = Number('9'.repeat(Config.MAX_DIGITS + 1));
            expect(format(overMax)).toMatch(/e\+\d+/);
        });
    });


    /**
     * 指数表記が正しく適用されることを確認
     */
    describe('指数表記', () => {
        /**
         * 大きな数は指数表記になることを確認
         * - 例: 100000000 → "1e+8" (指数表記)
         */
        it('8桁を超える大きな数は指数表記になる(100000000 → "1.0000000e+8")', () => {
            expect(format(100000000)).toBe('1.0000000e+8');
        });

        /**
         * 指数表記の境界値の確認
         * - 例: 99999999 (8桁) → "99999999" (通常表示)
         */
        it('指数表記の境界値の確認(99999999 → "99999999")', () => {
            expect(format(99999999)).toBe('99999999');
        });

        /**
         * 非常に大きな数も指数表記になることを確認
         * - 例: 1e20 → "1e+20" (指数表記)
         */
        it('非常に大きな数でも指数表記になる(1e20 → "1e+20")', () => {
            expect(format(1e20)).toMatch(/e\+/);
        });

        /**
         * 指数表記は指定桁数でフォーマットされる
         * - 例: 123456789 → "1.2345678e+8" (小数点以下7桁 + 整数部1桁 = 合計8桁)
         */
        it('指数表記は指定桁数でフォーマットされる(123456789 → "1.2345679e+8")', () => {
            const formatter8 = new NumberFormatter(8);
            const result = formatter8.formatForDisplay(123456789); // 9桁
            expect(result).toBe('1.2345679e+8'); // 小数点以下7桁 + 整数部1桁 = 合計8桁
        });

        /**
         * 小さすぎる数も指数表記になることを確認
         * - 例: 0.000000001234 → "1.234e-9" (指数表記)
         */
        it('小さすぎる小数も指数表記になる(0.000000001234 → "1.234e-9")', () => {
            expect(format(0.000000001234)).toMatch(/e-/);
        });

    });


    /**
     * 異常値の処理が正しく行われることを確認
     */
    describe('異常値', () => {
        /**
         * NaNは文字列"NaN"として返される
         */
        it('NaNは文字列"NaN"として返される', () => {
            expect(format(NaN)).toBe('NaN');
        });
    });
});